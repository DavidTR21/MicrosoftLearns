// Single quotes, used when methods expect just one character (char)
Console.WriteLine('b');

// Double quotes, used for strings
Console.WriteLine("This is a string");

// int type doesn't need another operator
Console.WriteLine(12345);

// numbers with float points can represent different type of numbers: float, double, decimal
Console.WriteLine(0.25F); // float

Console.WriteLine(2.625); // double

Console.WriteLine(12.3456789m); // decimal

// booleans (bool) save just true/false values

Console.WriteLine(true);
Console.WriteLine(false);

// -------------------------------------------

// string, char, int, float, double, decimal, bool
// You can define variables from different types in order to save different types of data

string text1;

text1 = "This is a string";

Console.WriteLine(text1);

int num1 = 12;
Console.WriteLine(num1);

// local variables

var message = "String value";
Console.WriteLine(message);